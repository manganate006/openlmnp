<div class="ctx-help">
    <h3>Gestion des emprunts</h3>
    <p>Renseignez vos prêts immobiliers pour que les intérêts et l'assurance emprunteur soient automatiquement déduits en charges.</p>

    <h3>Informations à saisir</h3>
    <ul>
        <li data-icon="&#x1F4B5;"><strong>Capital emprunté</strong> &mdash; Montant total du prêt</li>
        <li data-icon="&#x1F4C8;"><strong>Taux d'intérêt</strong> &mdash; Taux nominal annuel</li>
        <li data-icon="&#x1F4C5;"><strong>Durée</strong> &mdash; Nombre de mois du prêt</li>
        <li data-icon="&#x1F6E1;"><strong>Assurance</strong> &mdash; Montant mensuel de l'assurance emprunteur</li>
    </ul>

    <h3>Ce qui est déductible</h3>
    <p>Seuls les <strong>intérêts d'emprunt</strong> et l'<strong>assurance emprunteur</strong> sont déductibles (pas le capital remboursé). La quote-part s'applique si le bien est partiellement loué.</p>

    <h3>Tableau d'amortissement</h3>
    <p>Le tableau d'amortissement est calculé automatiquement. Vous pouvez le consulter en détail en cliquant sur un emprunt.</p>

    <div class="ctx-tip">
        <strong>Astuce :</strong> Les intérêts sont plus élevés en début de prêt, ce qui maximise la déduction fiscale les premières années.
    </div>
</div>
