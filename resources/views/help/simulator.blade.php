<div class="ctx-help">
    <h3>Simulateur micro-BIC vs régime réel</h3>
    <p>Comparez les deux régimes fiscaux pour déterminer lequel est le plus avantageux pour votre situation.</p>

    <h3>Les deux régimes</h3>
    <ul>
        <li data-icon="&#x1F4CA;"><strong>Micro-BIC</strong> &mdash; Abattement forfaitaire de 50 % (meublé classé) ou 30 % (non classé). Simple mais souvent moins avantageux.</li>
        <li data-icon="&#x1F4C8;"><strong>Régime réel</strong> &mdash; Déduction des charges réelles + amortissements. Plus avantageux quand les amortissements sont élevés.</li>
    </ul>

    <h3>Comment lire les résultats</h3>
    <ul>
        <li data-icon="&#x2705;"><strong>Économie</strong> &mdash; La différence entre les deux régimes. Si positive, le réel est plus avantageux.</li>
        <li data-icon="&#x1F4B0;"><strong>Base imposable</strong> &mdash; Le montant soumis à l'impôt dans chaque régime</li>
    </ul>

    <h3>Filtres</h3>
    <p>Sélectionnez l'année et le bien pour affiner la simulation. Les données sont calculées à partir de vos recettes, charges et amortissements réels.</p>

    <div class="ctx-tip">
        <strong>Astuce :</strong> Utilisez la projection pluriannuelle pour anticiper le moment où le micro-BIC redeviendra plus intéressant (quand les amortissements seront épuisés).
    </div>
</div>
