<div class="ctx-help">
    <h3>Assistant de création d'exercice</h3>
    <p>Cet assistant vous aide à créer un nouvel exercice fiscal et vérifie la cohérence de vos données avant le calcul.</p>

    <h3>Vérifications automatiques</h3>
    <ul>
        <li data-icon="&#x2705;"><strong>Recettes complètes</strong> &mdash; Toutes les recettes de l'année sont-elles saisies ?</li>
        <li data-icon="&#x2705;"><strong>Charges enregistrées</strong> &mdash; Les charges récurrentes sont-elles présentes ?</li>
        <li data-icon="&#x2705;"><strong>Justificatifs</strong> &mdash; Toutes les pièces sont-elles jointes ?</li>
        <li data-icon="&#x2705;"><strong>Cohérence</strong> &mdash; Les montants sont-ils plausibles par rapport à N-1 ?</li>
    </ul>

    <div class="ctx-tip">
        <strong>Astuce :</strong> Corrigez les anomalies signalées avant de valider l'exercice pour éviter des erreurs dans votre déclaration.
    </div>
</div>
