    <h3>Gestion du mobilier</h3>
    <p>Enregistrez tous les meubles et équipements achetés pour votre location meublée.</p>

    <h3>Règle des 600 EUR</h3>
    <ul>
        <li data-icon="&#x2B06;"><strong>Plus de 600 EUR TTC</strong> &mdash; Le meuble est <strong>amorti</strong> sur sa durée de vie (généralement 5 à 10 ans)</li>
        <li data-icon="&#x2B07;"><strong>Moins de 600 EUR TTC</strong> &mdash; Le meuble passe directement en <strong>charge</strong> déductible l'année d'achat</li>
    </ul>

    <h3>Durées d'amortissement courantes</h3>
    <ul>
        <li data-icon="&#x1F6CB;"><strong>Literie, canapé</strong> &mdash; 10 ans</li>
        <li data-icon="&#x1F4FA;"><strong>Électroménager, TV</strong> &mdash; 5 à 7 ans</li>
        <li data-icon="&#x1FA91;"><strong>Petit mobilier</strong> &mdash; 5 ans</li>
        <li data-icon="&#x1F5A5;"><strong>Informatique</strong> &mdash; 3 ans</li>
    </ul>

    <div class="ctx-tip">
        <strong>Astuce :</strong> Pour un meuble proche de 600 EUR, il est parfois plus avantageux de le passer en charge directe plutôt qu'en amortissement.
    </div>
