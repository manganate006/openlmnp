<div class="ctx-help">
    <h3>Détail de l'emprunt</h3>
    <p>Cette page affiche le tableau d'amortissement complet de votre prêt, mois par mois.</p>

    <h3>Lecture du tableau</h3>
    <ul>
        <li data-icon="&#x1F4B5;"><strong>Mensualité</strong> &mdash; Le montant fixe payé chaque mois (capital + intérêts)</li>
        <li data-icon="&#x1F4C9;"><strong>Part intérêts</strong> &mdash; Diminue progressivement au fil du prêt. C'est la part déductible fiscalement.</li>
        <li data-icon="&#x1F4C8;"><strong>Part capital</strong> &mdash; Augmente progressivement. Non déductible.</li>
        <li data-icon="&#x1F4CA;"><strong>Capital restant dû</strong> &mdash; Ce qu'il reste à rembourser</li>
    </ul>

    <h3>Déductibilité</h3>
    <p>OpenLMNP calcule automatiquement la part déductible (intérêts + assurance) en appliquant la quote-part du bien.</p>

    <div class="ctx-tip">
        <strong>Astuce :</strong> Les intérêts déductibles sont plus élevés les premières années du prêt, ce qui renforce l'intérêt du régime réel en début d'activité.
    </div>
</div>
