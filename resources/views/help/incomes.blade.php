<div class="ctx-help">
    <h3>Gestion des recettes</h3>
    <p>Enregistrez ici tous les loyers perçus via Airbnb, Booking ou en direct.</p>

    <h3>Saisie des recettes</h3>
    <ul>
        <li data-icon="&#x1F4B0;"><strong>Montant</strong> &mdash; Le montant net perçu (après commission plateforme)</li>
        <li data-icon="&#x1F4C5;"><strong>Date</strong> &mdash; Date d'encaissement du loyer</li>
        <li data-icon="&#x1F3E0;"><strong>Bien</strong> &mdash; Le bien concerné par cette recette</li>
        <li data-icon="&#x1F4CE;"><strong>Justificatif</strong> &mdash; Joignez le relevé ou la facture plateforme</li>
    </ul>

    <h3>Import CSV</h3>
    <p>Plutôt que de saisir manuellement, vous pouvez importer un fichier CSV depuis Airbnb via le menu <strong>Import Airbnb</strong>. Les doublons sont détectés automatiquement.</p>

    <div class="ctx-warning">
        <strong>Attention :</strong> Ne pas inclure la taxe de séjour dans les recettes &mdash; elle est collectée pour le compte de la commune et n'est pas un revenu.
    </div>
</div>
