<div class="ctx-help">
    <h3>Projection pluriannuelle</h3>
    <p>Visualisez l'évolution de votre résultat fiscal sur plusieurs années pour anticiper vos choix de régime.</p>

    <h3>Ce que montre la projection</h3>
    <ul>
        <li data-icon="&#x1F4C8;"><strong>Évolution du résultat réel</strong> &mdash; Comment votre résultat au régime réel évolue avec la diminution des amortissements</li>
        <li data-icon="&#x1F504;"><strong>Point de bascule</strong> &mdash; L'année où le micro-BIC devient plus avantageux que le régime réel</li>
        <li data-icon="&#x1F4B0;"><strong>Économie cumulée</strong> &mdash; Le gain total réalisé grâce au régime réel</li>
    </ul>

    <h3>Hypothèses</h3>
    <p>La projection suppose que vos recettes et charges restent stables. Ajustez les paramètres si vous prévoyez des changements (hausse des loyers, fin d'un emprunt, travaux...).</p>

    <div class="ctx-tip">
        <strong>Astuce :</strong> Pensez à re-consulter cette projection après chaque changement significatif (nouvel emprunt, travaux, nouveau bien).
    </div>
</div>
