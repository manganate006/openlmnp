    <h3>Travaux sur le bien</h3>
    <p>Les travaux réalisés sur votre bien locatif peuvent être amortis ou passés en charges selon leur nature.</p>

    <h3>Types de travaux</h3>
    <ul>
        <li data-icon="&#x2B06;"><strong>Travaux d'amélioration</strong> &mdash; Augmentent la valeur du bien (rénovation cuisine, salle de bain, isolation...). Amortis sur 10 à 15 ans.</li>
        <li data-icon="&#x1F527;"><strong>Travaux de réparation</strong> &mdash; Maintiennent le bien en état (remplacement chauffe-eau, peinture...). Passés en charges directes.</li>
    </ul>

    <h3>Informations à saisir</h3>
    <ul>
        <li data-icon="&#x1F4B5;"><strong>Montant TTC</strong> &mdash; Coût total des travaux</li>
        <li data-icon="&#x1F4C5;"><strong>Date</strong> &mdash; Date de réalisation ou de facturation</li>
        <li data-icon="&#x23F3;"><strong>Durée d'amortissement</strong> &mdash; Pour les travaux amortissables</li>
        <li data-icon="&#x1F4CE;"><strong>Justificatif</strong> &mdash; Facture de l'artisan ou du fournisseur</li>
    </ul>

    <div class="ctx-tip">
        <strong>Astuce :</strong> Les travaux réalisés avant le début de l'activité de location peuvent aussi être amortis, à condition de les dater correctement.
    </div>
</div>
