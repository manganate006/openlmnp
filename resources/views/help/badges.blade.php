<div class="ctx-help">
    <h3>Badges et accomplissements</h3>
    <p>Les badges récompensent votre progression dans la tenue de votre comptabilité LMNP.</p>

    <h3>Comment les obtenir</h3>
    <ul>
        <li data-icon="&#x2B50;"><strong>Badges de mise en route</strong> &mdash; Ajoutez votre premier bien, vos emprunts, vos meubles</li>
        <li data-icon="&#x1F4C5;"><strong>Badges de régularité</strong> &mdash; Saisissez vos recettes et charges chaque mois</li>
        <li data-icon="&#x1F3C6;"><strong>Badges de complétude</strong> &mdash; Joignez tous les justificatifs, créez un exercice fiscal</li>
        <li data-icon="&#x1F680;"><strong>Badges avancés</strong> &mdash; Utilisez le simulateur, la projection, générez un FEC</li>
    </ul>

    <h3>Score de complétude</h3>
    <p>La barre de progression indique votre niveau global de complétude. Plus vous êtes régulier et exhaustif, plus votre score augmente.</p>

    <div class="ctx-tip">
        <strong>Astuce :</strong> Consultez le badge « prochain à débloquer » pour savoir quelle action réaliser ensuite.
    </div>
</div>
